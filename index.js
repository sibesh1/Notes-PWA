//MIDDLEWARE
require("dotenv").config();
const express = require("express");
const app = express();
const cors = require("cors");
app.use(cors()); //CORS validation
const Note = require("./models/mongo");
app.use(express.json());
app.use(express.static("build")); //Frontend Page

